<?php
// enquanto condição faça  seus comandos
// para a variavel de inicial 1 até o valor final 10, incremente faça




// estrutura  for (para)
for($i = 1; $i <= 10; $i++){ // incremente $i++
    echo $i . PHP_EOL;
}





 //enquanto
$n = 1;
while ($n <= 10) {
    echo $n . "\n";
    $n = $n + 1;
}
echo "Fim";

